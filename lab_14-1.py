import turtle

window = turtle.Screen()
window.setup(1000, 1000)
window.screensize(800, 800)
window.title("lab 1")

murtle = turtle.Turtle()

murtle.forward(20)
murtle.right(90)
murtle.forward(125)
murtle.right(90)
murtle.forward(20)
murtle.right(90)
murtle.forward(125)

window.mainloop()