import turtle

window = turtle.Screen()
window.setup(500, 500)
window.screensize(800, 800)
window.title("lab 3")
window.bgcolor("grey")

murtle = turtle.Turtle()
murtle.pencolor("red")
murtle.shape("turtle")
murtle.forward(200)
murtle.right(120)
murtle.forward(200)
murtle.right(120)
murtle.forward(200)

window.mainloop()